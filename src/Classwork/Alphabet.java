package Classwork;

public class Alphabet {

    public static void main(String[] args){

        System.out.println("Uppercase Alphabet: ");
        for (char c = 'A'; c<='Z'; c++){
            System.out.print(c);
        }
        System.out.println("\nLowercase Alphabet: ");
        for(char c = 'a'; c <= 'z'; c++){
            System.out.print(c);
        }
    }
}
